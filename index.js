// Function expression called divide
const divide = function() {
    return 2000 / 100;
  }
  
  // Arrow function called square
  const square = (num) => {
    return num * num;
  }
  
  // Arrow function called add
  const add = (a, b) => {
    return a + b;
  }
  
  // Test the functions
  console.log(divide()); // Output: 20
  console.log(square(5)); // Output: 25
  console.log(add(3, 7)); // Output: 10